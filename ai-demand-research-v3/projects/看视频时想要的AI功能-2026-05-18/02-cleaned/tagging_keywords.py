def classify_text(text, patterns):
    """返回文本匹配到的所有标签"""
    if pd.isna(text):
        return []
    text = str(text).lower()
    labels = []
    for label, keywords in patterns.items():
        for kw in keywords:
            if kw in text:
                labels.append(label)
                break
    return labels


def tag_dataframe(df):
    """为DataFrame打上所有四维标签"""
    df = df.copy()
    df['layer1'] = df['full_text'].apply(lambda x: classify_text(x, LAYER1_PATTERNS))
    df['layer2'] = df['full_text'].apply(lambda x: classify_text(x, LAYER2_PATTERNS))
    df['emotions'] = df['full_text'].apply(lambda x: classify_text(x, EMOTION_PATTERNS))
    df['scenes'] = df['full_text'].apply(lambda x: classify_text(x, SCENE_PATTERNS))
    return df


